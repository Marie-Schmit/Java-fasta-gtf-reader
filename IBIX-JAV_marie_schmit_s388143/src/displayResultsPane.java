    //Display text stored in StringBuffer, in an ArrayList in text area
    public void displayText(ArrayList<StringBuffer> textContent) {
        int i;
        for (i = 0; i < textContent.size(); i++) {
            textArea.append(textContent.get(i).toString() + "\n");
        }
    }
    
